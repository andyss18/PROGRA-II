import Modelo.*;
import javax.swing.table.DefaultTableModel;
/**
 *
 * @author itsan
 */
abstract class Persona {
    private String nombres,direccion,apellidos,fecha_nacimiento;
    private int telefono;
    
    
    public Persona(){
    }

    public Persona(String nombres, String apellidos, String direccion, int telefono, String fecha_nacimiento) {
        this.nombres = nombres;
        this.apellidos = apellidos;
        this.direccion = direccion;
        this.telefono = telefono;
        this.fecha_nacimiento = fecha_nacimiento;
    }

    public String getNombres() {
        return nombres;
    }

    public void setNombres(String nombres) {
        this.nombres = nombres;
    }

    public String getApellidos() {
        return apellidos;
    }

    public void setApellidos(String apellidos) {
        this.apellidos = apellidos;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public int getTelefono() {
        return telefono;
    }

    public void setTelefono(int telefono) {
        this.telefono = telefono;
    }

    public String getFecha_nacimiento() {
        return fecha_nacimiento;
    }

    public void setFecha_nacimiento(String fecha_nacimiento) {
        this.fecha_nacimiento = fecha_nacimiento;
    }
    
    
    
    protected void Crear (){}
    protected DefaultTableModel leer(){return null;}
    protected void actualizar(){}
    protected void eliminar (){}
}

